#!/bin/bash -x
echo "Enter a no between 0 to 9 "
read num
if [ $num -eq 0 ]
then
echo "zero "$num
elif [ $num -eq 1 ]
then
echo "one "$num
elif [ $num -eq 2 ]
then
echo "two "$num
elif [ $num -eq 3 ]
then
echo "three "$num
elif [ $num -eq 4 ]
then
echo "four "$num
elif [ $num -eq 5 ]
then
echo "five "$num
elif [ $num -eq 6 ]
then
echo "six "$num
elif [ $num -eq 7 ]
then
echo "seven "$num
elif [ $num -eq 8 ]
then
echo "eight "$num
elif [ $num -eq 9 ]
then
echo "nine "$num
fi
